            // 获取此对象的所有属性信息
            PropertyInfo[] propertyInfos = this.GetType().GetProperties();

            string propertyStr = "";

            // 遍历所有属性，获取属性值并拼接成字符串
            for (int i = 0; i < propertyInfos.Length; i++)
            {
                //string name = propertyInfos[i].Name;

                propertyStr += propertyInfos[i].GetValue(item);

                // 如果不是最后一个属性，则添加逗号分隔符
                if (i != propertyInfos.Length - 1)
                    propertyStr += ",";
            }
            // 返回属性值字符串
            return propertyStr;
        }
