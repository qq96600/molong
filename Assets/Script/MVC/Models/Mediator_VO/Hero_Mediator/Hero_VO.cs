using Common;
using MVC;
/// <summary>
/// 角色数据
/// </summary>
public class Hero_VO : Base_VO
{
    /// <summary>
    /// 角色名称
    /// </summary>
    public string hero_name;
    /// <summary>
    /// 角色职业
    /// </summary>
    public string hero_type;
    /// <summary>
    /// 角色编号
    /// </summary>
    public string hero_index;
    /// <summary>
    /// 已有英雄列表
    /// </summary>
    public string hero_list;
    /// <summary>
    /// 角色等级
    /// </summary>
    public string hero_lv;
    /// <summary>
    /// 角色等级
    /// </summary>
    public int hero_Lv;
    /// <summary>
    /// 角色经验
    /// </summary>
    public string hero_exp;
    /// <summary>
    /// 角色经验
    /// </summary>
    public long hero_Exp;
    /// <summary>
    /// 角色上阵选择
    /// </summary>
    public string hero_pos;
    /// <summary>
    /// 备用栏
    /// </summary>
    public string hero_value;
    /// <summary>
    /// 获取英雄资源
    /// </summary>
    public string hero_material;
    /// <summary>
    /// 英雄资源 1金币 2砖石 3历练值 4声望
    /// </summary>
    public int[] hero_material_list;
    public override string[] Set_Instace_String()
    {
        return new string[]
        {
            GetStr(0),
            GetStr(SumSave.crt_user.uid),
            GetStr(hero_name),
            GetStr(hero_type),
            GetStr(hero_index),
            GetStr(hero_list),
            GetStr(hero_lv),
            GetStr(hero_exp),
            GetStr(hero_pos),
            GetStr(hero_value),
            GetStr(ArrayHelper.Data_Encryption(hero_material_list)),
        };
    }

    public override string[] Get_Update_Character()
    {
        return new string[]
        {
            "hero_type",
            "hero_list",
            "hero_lv",
            "hero_exp",
            "hero_pos",
            "hero_material"
        };
    }

    public override string[] Set_Uptade_String()
    {
        return new string[]
       {
            GetStr(hero_type),
            GetStr(hero_list),
            GetStr(hero_lv),
            GetStr(hero_exp),
            GetStr(hero_pos),
            GetStr(ArrayHelper.Data_Encryption(hero_material_list))
       };
    }

}
